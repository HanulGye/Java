package first;

public class Test3 {
	public static void main(String[]args) {
		//primitive type 첫번째는 숫자, 요거는 정수형(byte,short,int,long),실수형(double,float)으로 나뉨
		//				두번째는 문자는 한글자(char), 
		//				세번째는 참거짓(boolean)
		//변수 선언은 데이터타입 변수명 이렇게함. 절대 = 넣지않음.
		int x; x=3;
		//코딩에서 =은 대입연산자임. 같단 뜻이 아니라.
		int y; y=2*x+3;
		//일반 수학처럼 곱하기 생략안됨 ㅋㅋ
		System.out.println("y의 값"+y);
		
	}
	
}
